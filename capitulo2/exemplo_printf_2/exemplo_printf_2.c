/* Exemplo: escrita de dados na linguagem C */
#include <stdio.h>
#include <stdlib.h>

int main()
{
	int x = 10;

	// escrita de um valor inteiro
	printf("%d\n", x);

	float y = 5.0;

	// escrita de um valor inteiro e outro real
	printf("%d%f\n", x, y);
	// adicionando espa√ßo entre os valores
	printf("%d %f\n", x, y);

	return 0;
}